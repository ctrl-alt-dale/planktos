static void set_full_window(void) {
  cmd(0x21);
  cmd(0);
  cmd(DISPLAY_W - 1); /* column range: 0–127 */
  cmd(0x22);
  cmd(0);
  cmd(DISPLAY_PAGES - 1); /* page   range: 0–7   */
}

/* ── Framebuffer: bit-level write (unchanged) ───────────────────── *
 *                                                                    *
 *  put_pixel operates in absolute screen coordinates (0,0 = top-left)*
 *  Framebuffer layout in FRAM:                                       *
 *    addr = FRAM_FB_BASE + x + (y / 8) * DISPLAY_W                  *
 *    bit  = y % 8                                                    *
 *                                                                    *
 *  This function is the only path that writes to FRAM; all higher-   *
 *  level primitives call through here.                               *
 * ─────────────────────────────────────────────────────────────────── */
static void put_pixel(uint8_t x, uint8_t y, bool inv) {
  /* Bounds check — callers may pass clipped coords, guard anyway. */
  if (x >= DISPLAY_W || y >= DISPLAY_H) return;

  uint16_t addr = FRAM_FB_BASE
                  + (uint16_t)x
                  + ((uint16_t)y / 8u) * DISPLAY_W;
  uint8_t mask = 1u << ((uint8_t)y % 8u);
  uint8_t byte = fram_read_byte(addr);
  if (!inv) byte |= mask;
  else byte &= ~mask;
  fram_write_byte(addr, byte);
}

/* ── Cohen–Sutherland line clipping (absolute coordinates) ─────── */

static uint8_t compute_code(int16_t x, int16_t y) {
  uint8_t code = INSIDE;
  if (x < X_MIN) code |= LEFT;
  else if (x > X_MAX) code |= RIGHT;
  if (y < Y_MIN) code |= TOP;
  else if (y > Y_MAX) code |= BOTTOM;
  return code;
}

/*
 * clip_line — Cohen–Sutherland.
 * Works with signed int16 internally so intermediate products do not
 * overflow on AVR (the screen is 128×64; intermediate values stay well
 * within int16 range).
 * Returns 1 if the segment (partially) lies inside the viewport,
 * 0 if it is entirely outside.
 */
static uint8_t clip_line(int16_t *x0, int16_t *y0,
                         int16_t *x1, int16_t *y1) {
  uint8_t code0 = compute_code(*x0, *y0);
  uint8_t code1 = compute_code(*x1, *y1);

  while (1) {
    if (!(code0 | code1)) return 1; /* trivially inside  */
    if (code0 & code1) return 0;    /* trivially outside */

    uint8_t out = code0 ? code0 : code1;

    int32_t dx = (int32_t)(*x1 - *x0);
    int32_t dy = (int32_t)(*y1 - *y0);

    /* Degenerate: zero-length projection on the clipping axis → reject */
    if ((out & (TOP | BOTTOM)) && dy == 0) return 0;
    if ((out & (LEFT | RIGHT)) && dx == 0) return 0;

    int16_t x, y;

    if (out & TOP) {
      x = (int16_t)(*x0 + dx * ((int32_t)Y_MIN - *y0) / dy);
      y = Y_MIN; /* top edge: y = 0 */
    } else if (out & BOTTOM) {
      x = (int16_t)(*x0 + dx * ((int32_t)Y_MAX - *y0) / dy);
      y = Y_MAX; /* bottom edge: y = 63 */
    } else if (out & RIGHT) {
      y = (int16_t)(*y0 + dy * ((int32_t)X_MAX - *x0) / dx);
      x = X_MAX;
    } else {
      y = (int16_t)(*y0 + dy * ((int32_t)X_MIN - *x0) / dx);
      x = X_MIN;
    }

    if (out == code0) {
      *x0 = x;
      *y0 = y;
      code0 = compute_code(*x0, *y0);
    } else {
      *x1 = x;
      *y1 = y;
      code1 = compute_code(*x1, *y1);
    }
  }
}

/* ── Internal Bresenham line (absolute coords, after clipping) ─── */

static void line_abs(int16_t x0, int16_t y0,
                     int16_t x1, int16_t y1, bool inv) {
  if (!clip_line(&x0, &y0, &x1, &y1)) return;

  int16_t dx = abs(x1 - x0);
  int16_t dy = abs(y1 - y0);
  int16_t sx = (x0 < x1) ? 1 : -1;
  int16_t sy = (y0 < y1) ? 1 : -1;
  int16_t err = dx - dy;

  while (1) {
    /* Coordinates are guaranteed in-range after clipping. */
    put_pixel((uint8_t)x0, (uint8_t)y0, inv);
    if (x0 == x1 && y0 == y1) break;
    int16_t e2 = err << 1;
    if (e2 > -dy) {
      err -= dy;
      x0 += sx;
    }
    if (e2 < dx) {
      err += dx;
      y0 += sy;
    }
  }
}

/* ── Public API ─────────────────────────────────────────────────── */

void display_init(void) {
  twi_init();
  delay(100);
  cmd_batch(init_seq, sizeof(init_seq));
  fram_init();
  clf();
  display_render();
}

/* clf — zero the FRAM framebuffer, one 128-byte page at a time */
void clf(void) {
  uint8_t zeros[DISPLAY_W];
  memset(zeros, 0, DISPLAY_W);
  for (uint8_t page = 0; page < DISPLAY_PAGES; page++)
    fram_write_buf(FRAM_FB_BASE + (uint16_t)page * DISPLAY_W,
                   zeros, DISPLAY_W);
}

/* cls — clear framebuffer and push to display */
void cls(void) {
  clf();
  display_render();
}

/* display_render — stream FRAM framebuffer to SSD1306, one page at a time */
void display_render(void) {
  uint8_t buf[DISPLAY_W];
  set_full_window();
  for (uint8_t page = 0; page < DISPLAY_PAGES; page++) {
    fram_read_buf(FRAM_FB_BASE + (uint16_t)page * DISPLAY_W, buf, DISPLAY_W);
    twi_start();
    twi_write(SLA_W);
    twi_write(0x40); /* data stream control byte */
    for (uint16_t i = 0; i < DISPLAY_W; i++) twi_write(buf[i]);
    twi_stop();
  }
}


/* draw_pixel — plot a single pixel at absolute (x, y) */
void draw_pixel(uint8_t x, uint8_t y, bool inv) {
  put_pixel(x, y, inv);
}

void draw_boot(uint8_t offsetX, uint8_t offsetY) {
  for (uint8_t y = 0; y < DISPLAY_H; y++) {
    for (uint8_t byte_idx = 0; byte_idx < BYTES_PER_ROW; byte_idx++) {
      uint8_t b = pgm_read_byte(&planktos[y * BYTES_PER_ROW + byte_idx]);

      for (uint8_t bit = 0; bit < 8; bit++) {
        if (b & (0x80 >> bit)) {
          uint8_t x = (byte_idx << 3) | bit;
          put_pixel(offsetX + x, offsetY + y, false);
        }
      }
    }
  }
}



/* draw_line — Bresenham line from (x0,y0) to (x1,y1), with clipping */
void draw_line(uint8_t x0, uint8_t y0,
               uint8_t x1, uint8_t y1, bool inv) {
  line_abs((int16_t)x0, (int16_t)y0,
           (int16_t)x1, (int16_t)y1, inv);
}

/* draw_hline — horizontal line from x=x0 to x=x1 at row y (inclusive) */
void draw_hline(uint8_t x0, uint8_t x1, uint8_t y, bool inv) {
  if (y >= DISPLAY_H) return;
  if (x0 > x1) {
    uint8_t t = x0;
    x0 = x1;
    x1 = t;
  } /* normalise */
  if (x1 >= DISPLAY_W) x1 = DISPLAY_W - 1;
  for (uint8_t x = x0; x <= x1; x++)
    put_pixel(x, y, inv);
}

/* draw_vline — vertical line from y=y0 to y=y1 at column x (inclusive) */
void draw_vline(uint8_t y0, uint8_t y1, uint8_t x, bool inv) {
  if (x >= DISPLAY_W) return;
  if (y0 > y1) {
    uint8_t t = y0;
    y0 = y1;
    y1 = t;
  } /* normalise */
  if (y1 >= DISPLAY_H) y1 = DISPLAY_H - 1;
  for (uint8_t y = y0; y <= y1; y++)
    put_pixel(x, y, inv);
}

/* draw_rect — outline or filled rectangle.
 * (x, y) = top-left corner, w = width, h = height (in pixels). */
