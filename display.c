/* ── SSD1306 command helpers (unchanged) ────────────────────────── */

static void cmd(uint8_t c) {
  twi_start();
  twi_write(SLA_W);
  twi_write(0x00);
  twi_write(c);
  twi_stop();
}

static void cmd_batch(const uint8_t *cmds, uint8_t len) {
  twi_start();
  twi_write(SLA_W);
  twi_write(0x00);
  for (uint8_t i = 0; i < len; i++) twi_write(cmds[i]);
  twi_stop();
}

